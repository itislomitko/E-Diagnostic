                <p class="text-center">
               <big><b>Menjaga Paru-Paru Untuk Kesehatan Sistem Pernafasan</big></b>
                </p>
				<p class="text-justify">
				Paru-paru merupakan bagian dari organ tubuh yang berfungsi untuk melakukan respirasi atau pertukaran oksigen yang terkandung di dalam udara yang masuk ke dalam darah dan karbondioksida yang dibuang melalui mulut atau hidung kita. Begitu pentingnya bagian tubuh ini dalam sistem pernafasan, menjaganya agar tetap dalam kondisi baik dan sehat, akan membuatnya selalu berfungsi sesuai dengan seharusnya. Dengan kondisi udara yang semakin memburuk saat ini, terlebih lagi di daerah perkotaan dengan berbagai polusi, baik kendaraan bermotor maupun asap pabrik, tentunya sangat tidak baik bagi sistem pernafasan kita, terutama paru-paru.
				</p>
				<p class="text-justify">
				Berikut ini adalah beberapa tips yang dapat dilakukan untuk menjaga kesehatan paru-paru:
				</p>
				<p class="text-justify">
				<b>1. Biasakan menghirup udara melalui hidung.</b> Hidung telah dilengkapi dengan filter atau penyaring berupa bulu-bulu halus yang sangat berguna untuk menyaring udara yang masuk ke dalam tubuh. Jika terdapat kotoran seperti partikel debu dalam udara yang terhirup, maka akan tertahan pada bulu-bulu tersebut dan diendapkan dengan sedikit cairan di bagian hidung yang tidak masuk ke dalam saluran pernapasan. Kotoran yang menjadi endapan ini biasa kita sebut dengan upil.
				</p>
				<p class="text-justify">
				<b>2. Merangkakkan badan di lantai pada saat beraktifitas seperti membaca buku, menulis, atau menggunakan laptop merupakan kebiasaan yang tidak baik untuk paru-paru.</b> Posisi yang dimaksud adalah merentangkan badan dengan dada berada di bawah. Apabila sering dilakukan dapat mengakibatkan gangguan pada organ pernapasan termasuk paru-paru. Pada posisi tersebut keadaan paru-paru terhimpit atau terjepit beban tubuh, sehingga saluran paru-paru tidak dapat secara maksimal dalam menerima udara. Bahkan 25 % saluran tidak dapat teraliri oksigen, sehingga 75 % saluran lainnya menjadi berat. Hal ini dapat menyebabkan penyakit bronkitis dan paru-paru basah.
				</p>
				<p class="text-justify">
				<b>3. Hindari asap yang berbau menyengat.</b> Asap merupakan bentuk udara yang dengan partikel udara yang mengikat zat lain seperti debu halus, karbon, nitrogen, bakteri, virus, dll. Misalnya asap kendaraan merupakan udara berkarbon sisa pembakaran kendaraan bermotor, apabila terhirup dan masuk ke dalam paru-paru dapat berakibat pada berkurangnya fungsi organ paru-paru.
				</p>
				<p class="text-justify">
				<b>4. Perbanyaklah menghirup udara segar dan udara bersih yang bisa kita jumpai di pagi hari.</b> Atau juga bisa didapat di daerah yang bebas polusi, misalnya di daerah pegunungan atau tempat yang rimbun dengan pepohonan yang banyak mengandung oksigen dan udara bersih. Buatlah jadwal rutin tiap sebulan sekali atau dua kali untuk menyempatkan pergi ke daerah yang memiliki udara segar dan bersih.
				</p>
				<p class="text-justify">
				<b>5. Hindarilah rokok.</b> Dengan menghindarinya berarti mencegah peluang terjangkitnya penyakit yang disebabkan oleh rokok. Membebaskan hidup tanpa asap rokok akan membuat sistem pernafasan kita menjadi lebih bersih dan sehat.
				</p>
				<p class="text-justify">
				<b>6. Hindari makanan dan minuman yang mengandung soda.</b> Kandungannya tidak baik bagi kesehatan paru-paru. Termasuk semua jenis minuman berkarbonasi sangat tidak baik untuk paru-paru.
				</p>
				<p class "text-justify">
				<b>7. Faktor emosi pun harus dijaga.</b> Situasi yang pelik dan sikap yang emosional dapat berpengaruh pada pernafasan kita. Biasanya nafas menjadi mudah tersengal-sengal, tidak teratur, bahkan sesak nafas. Seseorang yang memiliki sikap sabar biasanya memiliki paru-paru yang lebih baik.
				</p>
